/* A program to demonstrate the use of the multiplication operator*/

public class Multiplication_Operator {

  public static void main(String args[]) {
    int x = 4, y = 7, z = 3;

    System.out.println("6*6 equals to " + (6 * 6));
    System.out.println("x*z equals to " + (x * z));
    System.out.println("z*y equals to " + (z * y));
  }
}
