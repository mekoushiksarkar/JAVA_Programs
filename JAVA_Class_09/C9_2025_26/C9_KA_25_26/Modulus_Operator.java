/* A program to demonstrate the use of the Remainder(Modulus) Operator*/

public class Modulus_Operator {

  public static void main(String args[]) {
    int x = 31, y = 55;
    float z = 22.4f;

    System.out.println("45 % 6 equals to " + (45 % 6));
    System.out.println("y % x equals to " + (y % x));
    System.out.println("x % z equals to " + (x % z));
  }
}
