/* A program to to demonstrate the use of the addition operator*/

public class Addition_Operator {

  public static void main(String args[]) {
    int a = 5, b = 7, c = 9;
    System.out.println("12+6 equals to " + (12 + 6));
    System.out.println("a+b equals to " + (a + b));
    System.out.println("b+c equals to " + (b + c));
  }
}
