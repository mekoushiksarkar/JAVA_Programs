/* A program to demonstrate the use of the division operator*/

public class Division_Operator {

  public static void main(String args[]) {
    System.out.println("12/6 equals to " + (12 / 6));
    System.out.println("13.0/6 equals to " + (13.0 / 6));
    System.out.println("17/3.0 equals to " + (17 / 3.0));
    System.out.println("20.0/2.0 equals to " + (20.0 / 2.0));
    System.out.println("18/6 equals to " + (18 / 6));
  }
}
