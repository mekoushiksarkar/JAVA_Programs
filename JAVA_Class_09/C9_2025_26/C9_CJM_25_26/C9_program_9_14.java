// A nested for loop pattern

class NestedLoop {

  public static void main(String[] args) {
    int i, j;
    for (i = 1; i <= 5; i++) {
      for (j = 1; j <= 5; j++) 
        System.out.print(i + "");
      System.out.println();
    }
  }
}

