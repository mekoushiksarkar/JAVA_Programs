/* A program to display the values of various date types*/

public class Data_types {

  public static void main(String args[]) {
    byte byte_value = 56;
    short short_value = 6543;
    int integer_value = 948372;
    long long_value = 674382937;
    float float_value = 78.65f;
    double double_value = 67544.9876;
    char char_alphabet = 'D';
    String String_name = "Cricketer";
    boolean boolean_value = false;

    System.out.println("The byte value is " + byte_value);
    System.out.println("The short value is " + short_value);
    System.out.println("The integer value is " + integer_value);
    System.out.println("The long value is " + long_value);
    System.out.println("The float value is " + float_value);
    System.out.println("The double value is " + double_value);
    System.out.println("The char alphabet is " + char_alphabet);
    System.out.println("The string name is " + String_name);
    System.out.println("The boolean value is " + boolean_value);
  }
}
